	fmt.Printf("\n%s", utils.DefaultTheme.Format("Enter your choice (1-2): ", "brightGreen"))

	var key []byte
	var err error
	choice := ""
	if input, err := bufio.NewReader(os.Stdin).ReadString('\n'); err == nil {
		choice = strings.TrimSpace(input)
	}


	// Show final result in different formats
	v.AddStep("Step 7: Final Result")
	v.AddStep("------------------")
	v.AddTextStep("Final Result (Hex)", hex.EncodeToString(result))
	v.AddTextStep("Final Result (Base64)", base64.StdEncoding.EncodeToString(result))


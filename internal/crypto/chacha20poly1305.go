		fmt.Printf("%s", utils.DefaultTheme.Format("Enter 32-byte key in hex format: ", "brightGreen"))
		keyHex := ""
		if input, err := bufio.NewReader(os.Stdin).ReadString('\n'); err == nil {
			keyHex = strings.TrimSpace(input)
		}
